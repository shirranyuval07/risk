package com.example.demo.network.shared;

public enum GameAction {
    // ==========================================
    // פעולות ניהול חדרים (Room Management)
    // ==========================================
    CREATE_ROOM,         // בקשה ליצירת חדר
    ROOM_CREATED,        // תגובת שרת: החדר נוצר בהצלחה
    JOIN_ROOM,           // בקשה להצטרפות לחדר
    JOIN_ROOM_SUCCESS,   // תגובת שרת: הצטרפות הצליחה
    PLAYER_JOINED,       // הודעת שרת לכולם: שחקן חדש הצטרף
    ERROR,               // שגיאה כללית (למשל חדר לא נמצא)

    // ==========================================
    // ניהול מצב משחק כללי (Game State)
    // ==========================================
    START_GAME,          // בקשה להתחלת משחק
    GAME_STARTED,        // תגובת שרת: המשחק התחיל
    GAME_ACTION,         // סוג הודעה כללי לפעולות בתוך המשחק (כמו שמוגדר אצלך)
    NEXT_PHASE,          // מעבר שלב (Setup -> Draft -> Attack וכו')
    NEXT_TURN,           // מעבר תור לשחקן הבא

    // ==========================================
    // פעולות ספציפיות על הלוח (In-Game Actions)
    // ==========================================
    SETUP_PLACE,         // הצבת חייל בשלב ההתחלתי
    DRAFT,               // תגבור חיילים בשלב ה-Draft
    ATTACK_REQ,          // בקשת התקפה מול שחקן אחר
    BATTLE_RESULT,       // תוצאת הקרב שחוזרת מהשרת
    CONQUEST_MOVE,       // העברת חיילים לאחר כיבוש טריטוריה
    FORTIFY,             // העברת חיילים בסוף התור
}